#include<bits/stdc++.h>
using namespace std;
int main() {
    long long int N1,N2;
    cin >> N1 >> N2;
    cout << N1 << " + " << N2 << " = " << N1 + N2 << endl;
    cout << N1 << " * " << N2 << " = " << N1 * N2 << endl;
    cout << N1 << " - " << N2 << " = " << N1 - N2 << endl;
    return 0;
}